print(12 + 3) # 15
print(12 - 3) # 9
print(12 * 3) # 36
print(12 / 3) # 4.0 (division operator will give float result)


print(2 ** 3)   # 8 (Exponentiation/power operator)
print(12 % 9)   # 3 (Modulus operator , will gives the remainder)
print(13 // 2)  # 6  (Floor division , rounds the result to the nearest whole number)
print(13/2)     # 6.5